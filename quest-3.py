n1 = int(input('Insira o primeiro número: '))
n2 = int(input('Insira o segundo número: '))

soma = n1 + n2
prod = n1 * n2
dif = n1 - n2
quo = n1 / n2
mod = n1 % n2

print(f'A soma de {n1} e {n2} = {soma}\nO produto de {n1} e {n2} = {prod}\nA diferença de {n1} e {n2} = {dif}\nO quociente de {n1} e {n2} = {quo}\nO resto da divisão de {n1} e {n2} = {mod}')
